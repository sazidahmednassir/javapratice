package bd.sazid;

public class JavaOperators {

	public static void main(String[] args) {
		
		int a=10;
		int b=20;
		
		System.out.println(a+b);
		System.out.println(b+a);
		System.out.println(b-a);
		System.out.println(b/a);
		System.out.println(b%a);
		System.out.println(a*b);
		
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		System.out.println(a==b);
		System.out.println(a!=b);
		
		
		boolean x=true;
		boolean y=false;
		
		System.out.println(x&&y);
		System.out.println(x||y);
		System.out.println(!x);
		System.out.println(!y);
		
		
		int c;
		
		c=a;
		
		System.out.println(c);
		
		c++;
		
		
		System.out.println(c);
		
		c+=a;
		
		System.out.println(c);
		
        c-=a;
		
		System.out.println(c);
		
        c*=a;
		
		System.out.println(c);
		
		c/=a;
			
		System.out.println(c);
	}
}
